
// Named function
function add1(a,b){
    return a+b
}


// Anonymous Function returns pointer to the variable add
const add2 = function(c,d){
    return c+d;
}


// Arrow function

const add3 = (e,f)=>{
    return e+f;

}



console.log(add1(21,44));
console.log(add2(45,34));
console.log(add3(8,7));



